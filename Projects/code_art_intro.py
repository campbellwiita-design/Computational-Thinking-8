# ### SETUP ###
import turtle
# ###############################################

t = turtle.Turtle()
t.penup()
t.goto(-100, -100)
t.color("purple")
t.pendown()

t.forward(50)
t.left(360)
t.forward(50)
t.left(360)
t.forward(50)
t.left(360)
t.forward(50)
t.left(360)
t.forward(50)
t.left(360)

# ###############################################
# ### ENDING ###
turtle.exitonclick()
